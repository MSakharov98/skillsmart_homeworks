# Пример 1: Наследование с ограничением
# Пример:
#
# "Гость является пользователем, но не может редактировать данные."
#
# Здесь гость — это подтип пользователя, но с ограниченными правами.
# Он наследует базовые свойства (например, просмотр страниц), но ограничивает доступ к другим действиям. Это типичное ограничение поведения в подклассе.
#
# Пример 2: Наследование с расширением
# Пример:
#
# "Продвинутый калькулятор является калькулятором, но умеет ещё строить графики и решать уравнения."
#
# Здесь продвинутый калькулятор — это расширенный вариант базового калькулятора. Он сохраняет все базовые функции (сложение, вычитание), но добавляет новые возможности, выходящие за рамки базового класса.